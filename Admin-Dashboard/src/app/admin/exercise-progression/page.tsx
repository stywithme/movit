'use client';

import { useEffect, useState, useCallback } from 'react';
import { useRouter } from 'next/navigation';
import { Button, Badge, Input } from '@/components/ui';

const ARCHETYPE_LABELS: Record<string, string> = {
  weighted_strength: 'Weighted Strength',
  bodyweight_dynamic: 'Bodyweight Dynamic',
  isometric_hold: 'Isometric Hold',
  mobility_rom: 'Mobility / ROM',
  motor_control: 'Motor Control',
};

const ARCHETYPE_COLORS: Record<string, 'primary' | 'purple' | 'teal' | 'default'> = {
  weighted_strength: 'primary',
  bodyweight_dynamic: 'purple',
  isometric_hold: 'teal',
  mobility_rom: 'default',
  motor_control: 'default',
};

interface ExerciseWithProfile {
  id: string;
  name: { en: string; ar: string };
  slug: string;
  archetype: string | null;
  hasProfile: boolean;
  profile: {
    id: string;
    archetype: string;
    isAutoGenerated: boolean;
    allowedAxes: string[];
  } | null;
}

export default function ExerciseProgressionPage() {
  const router = useRouter();
  const [exercises, setExercises] = useState<ExerciseWithProfile[]>([]);
  const [loading, setLoading] = useState(true);
  const [search, setSearch] = useState('');
  const [filterArchetype, setFilterArchetype] = useState('');
  const [filterStatus, setFilterStatus] = useState('');
  const [bulkArchetype, setBulkArchetype] = useState('');
  const [selectedIds, setSelectedIds] = useState<Set<string>>(new Set());
  const [saving, setSaving] = useState(false);

  const fetchData = useCallback(async () => {
    setLoading(true);
    try {
      const url = `/api/admin/exercise-progression/exercises${search ? `?search=${encodeURIComponent(search)}` : ''}`;
      const res = await fetch(url);
      const data = await res.json();
      if (data.success) setExercises(data.data);
    } catch (error) {
      console.error('Error fetching data:', error);
    } finally {
      setLoading(false);
    }
  }, [search]);

  useEffect(() => {
    fetchData();
  }, [fetchData]);

  const handleSetArchetype = async (exerciseId: string, archetype: string) => {
    try {
      const res = await fetch(`/api/admin/exercise-progression/${exerciseId}/archetype`, {
        method: 'PUT',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ archetype }),
      });
      const data = await res.json();
      if (data.success) fetchData();
      else alert(data.error || 'Failed to set archetype');
    } catch (error) {
      console.error('Error setting archetype:', error);
    }
  };

  const handleBulkGenerate = async () => {
    if (!bulkArchetype || selectedIds.size === 0) return;
    setSaving(true);
    try {
      const res = await fetch('/api/admin/exercise-progression/bulk-generate', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ exerciseIds: Array.from(selectedIds), archetype: bulkArchetype }),
      });
      const data = await res.json();
      if (data.success) {
        setSelectedIds(new Set());
        setBulkArchetype('');
        fetchData();
      } else {
        alert(data.error || 'Failed to bulk generate');
      }
    } catch (error) {
      console.error('Error in bulk generate:', error);
    } finally {
      setSaving(false);
    }
  };

  const toggleSelect = (id: string) => {
    setSelectedIds((prev) => {
      const next = new Set(prev);
      if (next.has(id)) next.delete(id);
      else next.add(id);
      return next;
    });
  };

  const filtered = exercises.filter((ex) => {
    if (filterArchetype && ex.profile?.archetype !== filterArchetype) return false;
    if (filterStatus === 'has_profile' && !ex.hasProfile) return false;
    if (filterStatus === 'no_profile' && ex.hasProfile) return false;
    return true;
  });

  const toggleSelectAll = () => {
    if (selectedIds.size === filtered.length) {
      setSelectedIds(new Set());
    } else {
      setSelectedIds(new Set(filtered.map((e) => e.id)));
    }
  };

  const stats = {
    total: exercises.length,
    withProfile: exercises.filter((e) => e.hasProfile).length,
    withoutProfile: exercises.filter((e) => !e.hasProfile).length,
  };

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-2xl font-bold text-gray-900">Exercise Progression</h1>
        <p className="text-gray-600 mt-1">
          Manage scientific progression profiles for exercises.
          This is the single source of truth for how each exercise progresses.
        </p>
      </div>

      {/* Stats */}
      <div className="grid grid-cols-3 gap-4">
        <div className="bg-white rounded-lg border p-4">
          <p className="text-sm text-gray-500">Total Exercises</p>
          <p className="text-2xl font-bold">{stats.total}</p>
        </div>
        <div className="bg-white rounded-lg border p-4">
          <p className="text-sm text-gray-500">With Profile</p>
          <p className="text-2xl font-bold text-green-600">{stats.withProfile}</p>
        </div>
        <div className="bg-white rounded-lg border p-4">
          <p className="text-sm text-gray-500">Missing Profile</p>
          <p className="text-2xl font-bold text-orange-600">{stats.withoutProfile}</p>
        </div>
      </div>

      {/* Filters */}
      <div className="bg-white rounded-lg border p-4">
        <div className="flex gap-4 items-end flex-wrap">
          <div className="flex-1 min-w-[200px]">
            <label className="block text-sm font-medium text-gray-700 mb-1">Search</label>
            <Input
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              placeholder="Search by slug..."
            />
          </div>
          <div className="w-48">
            <label className="block text-sm font-medium text-gray-700 mb-1">Archetype</label>
            <select
              value={filterArchetype}
              onChange={(e) => setFilterArchetype(e.target.value)}
              className="w-full border rounded-lg px-3 py-2 text-sm"
            >
              <option value="">All Archetypes</option>
              {Object.entries(ARCHETYPE_LABELS).map(([k, v]) => (
                <option key={k} value={k}>{v}</option>
              ))}
            </select>
          </div>
          <div className="w-48">
            <label className="block text-sm font-medium text-gray-700 mb-1">Profile Status</label>
            <select
              value={filterStatus}
              onChange={(e) => setFilterStatus(e.target.value)}
              className="w-full border rounded-lg px-3 py-2 text-sm"
            >
              <option value="">All</option>
              <option value="has_profile">Has Profile</option>
              <option value="no_profile">Missing Profile</option>
            </select>
          </div>
        </div>
      </div>

      {/* Bulk Actions */}
      {selectedIds.size > 0 && (
        <div className="bg-blue-50 rounded-lg border border-blue-200 p-4 flex items-center gap-4">
          <span className="text-sm font-medium text-blue-800">
            {selectedIds.size} exercise(s) selected
          </span>
          <select
            value={bulkArchetype}
            onChange={(e) => setBulkArchetype(e.target.value)}
            className="border rounded-lg px-3 py-1.5 text-sm"
          >
            <option value="">Select Archetype...</option>
            {Object.entries(ARCHETYPE_LABELS).map(([k, v]) => (
              <option key={k} value={k}>{v}</option>
            ))}
          </select>
          <Button
            onClick={handleBulkGenerate}
            disabled={!bulkArchetype || saving}
            size="sm"
          >
            {saving ? 'Generating...' : 'Generate Profiles'}
          </Button>
        </div>
      )}

      {/* Table */}
      <div className="bg-white rounded-lg shadow-sm border overflow-hidden">
        {loading ? (
          <div className="p-8 text-center text-gray-500">Loading...</div>
        ) : filtered.length === 0 ? (
          <div className="p-8 text-center text-gray-500">No exercises found.</div>
        ) : (
          <table className="w-full">
            <thead className="bg-gray-50 border-b">
              <tr>
                <th className="px-4 py-3 text-left">
                  <input
                    type="checkbox"
                    checked={selectedIds.size === filtered.length && filtered.length > 0}
                    onChange={toggleSelectAll}
                    className="h-4 w-4 rounded border-gray-300"
                  />
                </th>
                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 uppercase">Exercise</th>
                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 uppercase">Archetype</th>
                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 uppercase">Profile</th>
                <th className="px-4 py-3 text-left text-xs font-medium text-gray-500 uppercase">Axes</th>
                <th className="px-4 py-3 text-right text-xs font-medium text-gray-500 uppercase">Actions</th>
              </tr>
            </thead>
            <tbody className="divide-y divide-gray-200">
              {filtered.map((ex) => (
                <tr key={ex.id} className="hover:bg-gray-50">
                  <td className="px-4 py-3">
                    <input
                      type="checkbox"
                      checked={selectedIds.has(ex.id)}
                      onChange={() => toggleSelect(ex.id)}
                      className="h-4 w-4 rounded border-gray-300"
                    />
                  </td>
                  <td className="px-4 py-3">
                    <p className="font-medium text-gray-900">{(ex.name as any)?.en || ex.slug}</p>
                    <p className="text-xs text-gray-500">{ex.slug}</p>
                  </td>
                  <td className="px-4 py-3">
                    {ex.hasProfile && ex.profile ? (
                      <Badge variant={ARCHETYPE_COLORS[ex.profile.archetype] || 'default'}>
                        {ARCHETYPE_LABELS[ex.profile.archetype] || ex.profile.archetype}
                      </Badge>
                    ) : ex.archetype ? (
                      <span className="text-xs text-gray-400">{ARCHETYPE_LABELS[ex.archetype] || ex.archetype}</span>
                    ) : (
                      <select
                        className="text-xs border rounded px-2 py-1"
                        defaultValue=""
                        onChange={(e) => {
                          if (e.target.value) handleSetArchetype(ex.id, e.target.value);
                        }}
                      >
                        <option value="">Assign...</option>
                        {Object.entries(ARCHETYPE_LABELS).map(([k, v]) => (
                          <option key={k} value={k}>{v}</option>
                        ))}
                      </select>
                    )}
                  </td>
                  <td className="px-4 py-3">
                    {ex.hasProfile ? (
                      <Badge variant={ex.profile?.isAutoGenerated ? 'default' : 'primary'}>
                        {ex.profile?.isAutoGenerated ? 'Auto' : 'Custom'}
                      </Badge>
                    ) : (
                      <span className="text-xs text-orange-500 font-medium">Missing</span>
                    )}
                  </td>
                  <td className="px-4 py-3">
                    {ex.profile?.allowedAxes ? (
                      <div className="flex gap-1 flex-wrap">
                        {(ex.profile.allowedAxes as string[]).map((axis) => (
                          <span key={axis} className="text-xs bg-gray-100 text-gray-600 px-1.5 py-0.5 rounded">
                            {axis}
                          </span>
                        ))}
                      </div>
                    ) : (
                      <span className="text-xs text-gray-400">—</span>
                    )}
                  </td>
                  <td className="px-4 py-3 text-right">
                    <button
                      onClick={() => router.push(`/admin/exercise-progression/${ex.id}`)}
                      className="text-blue-600 hover:text-blue-800 text-sm font-medium"
                    >
                      {ex.hasProfile ? 'Configure' : 'Setup'}
                    </button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        )}
      </div>
    </div>
  );
}
